package Control_Statments;

public class ForLoop {
    public static void main(String[] args) {
        int i = 0;
        while (i<=100){
            System.out.print(i + " ");
            i++;
        }
        System.out.println("\nBy for Loop");

        for (int j = 0 ; j <= 100 ; j++){
            System.out.print(j +  " ");
        }
    }
}
