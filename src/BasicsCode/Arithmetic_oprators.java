package BasicsCode;

public class Arithmetic_oprators {
    public static void main(String[] args) {
        //Noramls operators
        int a = 12;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        //dot arithmetic operators
        double x = 22;
        double y = 7;
        System.out.println(x/y);
        //float operators
        double c = 22;
        float d = 7;
        System.out.println(c/d);
        //
    }
}
